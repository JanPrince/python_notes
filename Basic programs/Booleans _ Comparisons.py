#      comparison operators
"""
if i and j and variables,

i < j     less than
i <= j
i > j
i >= j     greater than or equal to
i == j      equality
i != j      not equal to

comparisons evaluate to booleans
"""
a = 45
b = 22
print(a < b)
print(a > b)
print(a <= b)

print(a != b)

print('\n')

"""
       Logic operators on boolean values - works using the truth tables
    not
    and
    or
"""

x = True
y = False
print(not x)
print(x and y)
print(x and x)
print(y and y)

print(x or y)
print(x or x)
print(y or y)